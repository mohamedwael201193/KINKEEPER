# Generates Guardian Mesh demo scenario assets (Windows SAPI + System.Drawing).
$ErrorActionPreference = "Stop"
$root = Join-Path $PSScriptRoot ".."
$audioDir = Join-Path $root "test-data"
$sampleDir = Join-Path $root "samples"
New-Item -ItemType Directory -Force -Path $audioDir, $sampleDir | Out-Null

Add-Type -AssemblyName System.Speech
Add-Type -AssemblyName System.Drawing

function Write-ScamAudio($path, $text) {
  $synth = New-Object System.Speech.Synthesis.SpeechSynthesizer
  $synth.SetOutputToWaveFile($path)
  $synth.Speak($text)
  $synth.Dispose()
  Write-Output "Audio: $path"
}

# Scenario A — IRS scam (existing)
Write-ScamAudio (Join-Path $audioDir "sentinel-scam-call.wav") `
  "Hello Margaret, this is Officer Smith from the IRS. Your social security number has been suspended due to suspicious activity. You must pay five thousand dollars in gift cards today or we will send police to your home."

