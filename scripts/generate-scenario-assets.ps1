# Scenario F — fake healthcare billing (image)
$healthPath = Join-Path $sampleDir "scenario-f-healthcare.png"
$bmp = New-Object System.Drawing.Bitmap 800, 520
$g = [System.Drawing.Graphics]::FromImage($bmp)
$g.Clear([System.Drawing.Color]::White)
$title = New-Object System.Drawing.Font("Arial", 20, [System.Drawing.FontStyle]::Bold)
$body = New-Object System.Drawing.Font("Arial", 13)
$g.DrawString("MEDICARE BENEFITS SUSPENSION NOTICE", $title, [System.Drawing.Brushes]::DarkRed, 40, 30)
$g.DrawString("Patient: Margaret Thompson", $body, [System.Drawing.Brushes]::Black, 40, 90)
$g.DrawString("Your Medicare Part B will be cancelled unless you verify your Social Security number.", $body, [System.Drawing.Brushes]::Black, 40, 130)
$g.DrawString("Pay $299 processing fee by phone with gift cards to restore coverage.", $body, [System.Drawing.Brushes]::Black, 40, 170)
$g.DrawString("Call 1-888-555-0177 immediately. Do not contact your doctor.", $body, [System.Drawing.Brushes]::Black, 40, 210)
$bmp.Save($healthPath, [System.Drawing.Imaging.ImageFormat]::Png)
$g.Dispose(); $bmp.Dispose()
Write-Output "Image: $healthPath"

